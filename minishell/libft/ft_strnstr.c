#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;
	char	*ptr;

	i = 0;
	ptr = (char *)haystack;
	if (!haystack || !needle)
		return (NULL);
	if (!(needle[0]))
		return (ptr);
	while (i < len && haystack[i])
	{
		j = 0;
		while (needle[j] && haystack[i + j] == needle[j] && (i + j) < len)
			j++;
		if (needle[j] == '\0')
			return (&ptr[i]);
		i++;
	}
	return (NULL);
}
