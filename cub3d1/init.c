#include "cub3d.h"

void init_mlx(t_data *data) 
{
    data->mlx = mlx_init(W, H, "42balls", false);
    if (!data->mlx)
    {
        free(data->mlx);
        exit(0);
    }
    data->img = mlx_new_image(data->mlx, W, H);
    if (!data->img)
    {
        mlx_close_window(data->mlx);
        free(data); 
    }
    if (mlx_image_to_window(data->mlx, data->img, 0, 0) < 0)
	{
		mlx_close_window(data->mlx);
		free(data); 
		exit(0);
	}
}

